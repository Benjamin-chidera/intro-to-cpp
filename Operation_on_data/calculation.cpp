#include <iostream>
using namespace std;

int main() {
    int addition = 3 + 3;

    cout << "Addition of 3 and 3 is: " << addition << endl;

    int substraction = 10 - 5;

    cout << "Subtraction of 10 and 5 is: " << substraction << endl;

    int multiplication = 2 * 4;

    cout << "Multiplication of 2 and 4 is: " << multiplication << endl;

    int division = 10 / 2;

    cout << "Division of 10 and 2 is: " << division << endl;

    int modulus = 10 % 3;

    cout << "Modulus of 10 and 3 is: " << modulus << endl;

    return 0;
}