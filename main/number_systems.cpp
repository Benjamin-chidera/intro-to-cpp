 #include <iostream>
using namespace std;

int main() {
    int num1 = 15; // decimal
    int num2 = 017; // octal
    int num3 = 0x0f; // hexadecimal
    int num4 = 0b00001111; // binary

    cout << "num1 = " << num1 << endl;
    cout << "num2 = " << num2 << endl;
    cout << "num3 = " << num3 << endl;
    cout << "num4 = " << num4 << endl;

    return 0;
}