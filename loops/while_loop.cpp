#include <iostream>
using namespace std;

int main() {
   int count = 0;

   while (count < 10)
    { // loop will run until count is less than 10
       cout << "The value of count is " << count << endl;
       count++;
    }
    
    
    

   cout << "The final value of count is " << count << endl;

   return 0;
}